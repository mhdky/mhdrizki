     */
    public function update(Request $request, Social $social)
    {
        $validateData = $request->validate([
            'telegram' => 'required|url',
            'email' => 'required|url',
            'facebook' => 'required|url',
            'instagram' => 'required|url',
            'linkedin' => 'required|url',
            'youtube' => 'required|url', 
            'github' => 'required|url'
        ]);

